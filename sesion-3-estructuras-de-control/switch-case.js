// Switch case

let nota = 5;

switch (nota) {
  case 5:
    console.log("Aprovado");
    break;
  case 4:
    console.log("Buen trabajo");
    break;
  case 3:
    console.log("Mejorable");
    break;
  case 2:
    console.log("Deficiente");
    break;
  case 1:
    console.log("Reprobado");
    break;
  default:
    console.log("Error!");
    break;
}
