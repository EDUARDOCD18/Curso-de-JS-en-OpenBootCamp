// If else + if else

let nota = 3;

if (nota === 5) {
  console.log("Aprovado");
} else if (nota === 4) {
  console.log("Buen trabajo");
} else if (nota === 3) {
  console.log("Mejorable");
} else if (nota === 2) {
  console.log("Deficiente");
} else if (nota === 1) {
  console.log("Reprobado");
} else {
  console.log("Error!");
}
